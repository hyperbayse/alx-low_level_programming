#include "3-calc.h"
/**
 * op_add - add two integers
 * @a: integer 1
 * @b: integer 2
 * Return: success
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two integers
 * @a: integer 1
 * @b: integer 2
 * Return: success
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: integer 1
 * @b: integer 2
 * Return: success
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two integers
 * @a: integer 1
 * @b: integer 2
 * Return: success
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two integers
 * @a: integer 1
 * @b: integer 2
 * Return: success
 */
int op_mod(int a, int b)
{
	return (a % b);
}
